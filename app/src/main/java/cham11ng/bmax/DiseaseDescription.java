public class DiseaseDescription {
    private String disease_name;
    private String disease_definition;
    private String d_id;


    public String getDisease_name() {
        return disease_name;
    }

    public void setDisease_name(String disease_name) {
        this.disease_name = disease_name;
    }

    public String getDisease_definition() {
        return disease_definition;
    }

    public void setDisease_definition(String disease_definition) {
        this.disease_definition = disease_definition;
    }

    public String getD_id() {
        return d_id;
    }

    public void setD_id(String d_id) {
        this.d_id = d_id;
    }
}
